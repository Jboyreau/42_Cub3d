#include "header.h"

t_v3_uv	vec3uv_addition(t_v3_uv *v0, t_v3_uv *v1)
{
	t_v3_uv	sum;

	sum.x = (*v0).x + (*v1).x;
	sum.y = (*v0).y + (*v1).y;
	sum.z = (*v0).z + (*v1).z;
	return (sum);
}
